my_string = input('Введите текст: ')
print('Количество символов в введёном тексте: ', len(my_string)) # C помощью команды len узнаем длину строки в символах
print(my_string .upper()) #В верхнем регистре
print(my_string .lower()) #В нижнем регистре
print(my_string .replace(' ', '')) #Без пробелов
print(my_string [0])
print(my_string [-1])

